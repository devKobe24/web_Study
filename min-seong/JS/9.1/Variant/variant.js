var num = 10 + 20;
console.log(num);

// 변수를 생성하고 값을 저장하는 문법에서 var 키워드나 let, const 키워드를 사용해 변수의 식별자를 지정하는 행위를 "변수를 선언한다"라고 합니다.
// 그리고 할당 연산자인 "=" 기호로 우변에 있는 값을 변수 공간에 대입(저장)하는 것을 "값을 할당한다"고 합니다.

// 변수는 초기에 값을 할당하지 않고 선언만 할 수 있습니다.
// 예를 들어, 다음과 같이 작성해도 변수 선언입니다.
var number;

// 또는 처음에 나왔던 코드처럼 선언과 할당을 같이 하는 것도 가능한데, 선언과 할당을 한 번에 하면 "변수를 초기화한다"고 합니다.

var evenNumber = 1 + 1; // 변수 evenNumber을 초기화합니다.

// 여기서 살펴본 키워드, 식별자, 연산자, 표현식, 값, 세미콜론, 선언, 할당, 초기화 등의 용어는 앞으로도 자주 등장하므로 기억!!